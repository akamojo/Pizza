    private void changeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeButtonActionPerformed
        
        int selectionIndex = deliverTable.getSelectionModel().getMinSelectionIndex();
        DeliverTableModel tableModel = (DeliverTableModel) deliverTable.getModel();
        int selectedId = tableModel.getSelectedId();
        Dostawca selectedDostawca = mapaDostawcow.get(selectedId);
        selectedDostawca.setPesel(Integer.parseInt(peselTextField.getText()));
        selectedDostawca.setImie(firstNameTextField.getText());
        selectedDostawca.setNazwisko(nameTextField.getText());
        Set<PrawoJazdy> selectedListaPrawaJazdy = new HashSet<PrawoJazdy>();
        if (carCheckBox.isSelected()) {
            selectedListaPrawaJazdy.add(PrawoJazdy.SAMOCHOD);
        }
        if (motorCheckBox.isSelected()) {
            selectedListaPrawaJazdy.add(PrawoJazdy.SKUTER);
        }
        selectedDostawca.setListaPrawaJazdy(selectedListaPrawaJazdy);
        ((DeliverTableModel) deliverTable.getModel()).fireTableDataChanged();
        deliverTable.getSelectionModel().setSelectionInterval(selectionIndex, selectionIndex);
        
    }//GEN-LAST:event_changeButtonActionPerformed

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        
        restauracja.dodajLosowegoDostawce();
        int selectedIndex = deliverTable.getRowCount() - 1;
        ((DeliverTableModel) deliverTable.getModel()).fireTableDataChanged();
        deliverTable.getSelectionModel().setSelectionInterval(selectedIndex, selectedIndex);
        
    }//GEN-LAST:event_addButtonActionPerformed

    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeButtonActionPerformed
        
        int selectionIndex = deliverTable.getSelectionModel().getMinSelectionIndex();
        if (selectionIndex >= 0) {
            DeliverTableModel tableModel = (DeliverTableModel) deliverTable.getModel();
            int selectedId = tableModel.getSelectedId();
            restauracja.usunDostawce(selectedId);
            ((DeliverTableModel) deliverTable.getModel()).fireTableDataChanged();
            int lastIndex = deliverTable.getRowCount() - 1;
            deliverTable.getSelectionModel().setSelectionInterval(lastIndex, lastIndex);
        }
        
    }//GEN-LAST:event_removeButtonActionPerformed

