    /**
     * Definicja TableModelu dla tabeli dostawców i przesłanianie metod
     * (Dziedziczącego z AbstractTableModel)
     */
    class DeliverTableModel extends AbstractTableModel {

        private final List<Dostawca> listaDostawcow;

        public DeliverTableModel(List<Dostawca> listaDostawcow) {
            this.listaDostawcow = listaDostawcow;
        }

        @Override
        public boolean isCellEditable(int rowIndex, int columnIndex) {
            return false;
        }

        @Override
        public void setValueAt(Object aValue, int rowIndex, int columnIndex) {

        }

        @Override
        public String getColumnName(int column) {
            String columnName = "";
            if (null != DeliverColumns.values()[column]) {
                switch (DeliverColumns.values()[column]) {
                    case ID:
                        columnName = "Id";
                        break;
                    case PESEL:
                        columnName = "Pesel";
                        break;
                    case FIRSTNAME:
                        columnName = "Imię";
                        break;
                    case NAME:
                        columnName = "Nazwisko";
                        break;
                    default:
                        break;
                }
            }
            return columnName;
        }

        @Override
        public Class<?> getColumnClass(int columnIndex) {
            if (DeliverColumns.values()[columnIndex] == DeliverColumns.ID || DeliverColumns.values()[columnIndex] == DeliverColumns.PESEL) {
                return Integer.class;
            } else {
                return String.class;
            }
        }

        @Override
        public int getColumnCount() {
            return DeliverColumns.values().length;
        }

        @Override
        public int getRowCount() {
            return listaDostawcow.size();
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Object result = null;
            Dostawca dostawca = listaDostawcow.get(rowIndex);
            if (null != DeliverColumns.values()[columnIndex]) {
                switch (DeliverColumns.values()[columnIndex]) {
                    case ID:
                        result = dostawca.getID();
                        break;
                    case PESEL:
                        result = dostawca.getPesel();
                        break;
                    case FIRSTNAME:
                        result = dostawca.getImie();
                        break;
                    case NAME:
                        result = dostawca.getNazwisko();
                        break;
                    default:
                        break;
                }
            }
            return result;
        }

        public int getSelectedId() {
            return (Integer) getValueAt(deliverTable.getSelectedRow(), DeliverColumns.ID.ordinal());
        }

    }

    /**
     * Uzupełnianie szczegółowych informacji o dostawcy na podstawie wyboru dokonanego w tabeli
     */
    private void selectDeliver() {

        int selectionIndex = deliverTable.getSelectionModel().getMinSelectionIndex();

        if (selectionIndex >= 0) {

            DeliverTableModel tableModel = (DeliverTableModel) deliverTable.getModel();
            int selectedId = tableModel.getSelectedId();
            Dostawca selectedDostawca = mapaDostawcow.get(selectedId);
            peselTextField.setText(Integer.toString(selectedDostawca.getPesel()));
            firstNameTextField.setText(selectedDostawca.getImie());
            nameTextField.setText(selectedDostawca.getNazwisko());
            carCheckBox.setSelected(false);
            motorCheckBox.setSelected(false);

            for (PrawoJazdy i : selectedDostawca.getListaPrawaJazdy()) {
                if (i == PrawoJazdy.SAMOCHOD) {
                    carCheckBox.setSelected(true);
                } else if (i == PrawoJazdy.SKUTER) {
                    motorCheckBox.setSelected(true);
                }
            }
            
            orderTable.removeAll();
            orderTable.setModel(new OrderTableModel(selectedDostawca.getListaZamowien()));

        }

    }

    /**
     * Definicja TableModelu dla tabeli zamówień (innej dla każdego dostawcy)
     */
    class OrderTableModel extends AbstractTableModel {

        private final List<Zamowienie> listaZamowien;

        public OrderTableModel(List<Zamowienie> listaZamowien) {
            this.listaZamowien = listaZamowien;
        }

        @Override
        public boolean isCellEditable(int rowIndex, int columnIndex) {
            return false;
        }

        @Override
        public void setValueAt(Object aValue, int rowIndex, int columnIndex) {

        }

        @Override
        public String getColumnName(int column) {
            String columnName = "";
            if (null != OrderColumns.values()[column]) {
                switch (OrderColumns.values()[column]) {
                    case NUMER:
                        columnName = "Numer";
                        break;
                    case KOSZT:
                        columnName = "Koszt";
                        break;
                    case STATUS:
                        columnName = "Status";
                        break;
                    case KLIENT:
                        columnName = "Dostawca";
                        break;
                    default:
                        break;
                }
            }
            return columnName;
        }

        @Override
        public Class<?> getColumnClass(int columnIndex) {
            if (OrderColumns.values()[columnIndex] == OrderColumns.NUMER) {
                return Integer.class;
            } else if (OrderColumns.values()[columnIndex] == OrderColumns.KOSZT) {
                return Float.class;
            } else {
                return String.class;
            }
        }

        @Override
        public int getColumnCount() {
            return OrderColumns.values().length;
        }

        @Override
        public int getRowCount() {
            return listaZamowien.size();
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Object result = null;
            Zamowienie zamowienie = listaZamowien.get(rowIndex);
            if (null != OrderColumns.values()[columnIndex]) {
                switch (OrderColumns.values()[columnIndex]) {
                    case NUMER:
                        result = zamowienie.getNumer();
                        break;
                    case STATUS:
                        result = zamowienie.getStatus().toString();
                        break;
                    case KOSZT:
                        result = zamowienie.getKoszt();
                        break;
                    case KLIENT:
                    	int idDostawcy = zamowienie.getIdDostawcy();
                        if (idDostawcy == 0) {
                            result = "Nie przydzielono";
                        } else {
                        	Dostawca dostawca = mapaDostawcow.get(idDostawcy);
                            result = dostawca.getImie() + " " + dostawca.getNazwisko();
                        }
                        break;
                    default:
                        break;
                }
            }
            return result;
        }

        public int getSelectedNr() {
            return (Integer) getValueAt(orderTable.getSelectedRow(), OrderColumns.NUMER.ordinal());
        }

    }
    

